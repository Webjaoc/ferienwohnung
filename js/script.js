const languageButtons = document.querySelectorAll(".lang-button");
const menuToggle = document.querySelector(".menu-toggle");
const siteHeader = document.querySelector(".site-header");
const siteNav = document.querySelector(".site-nav");
const visitCounter = document.getElementById("visit-counter-value");
const lightbox = document.getElementById("lightbox");
const lightboxImage = document.getElementById("lightbox-image");
const lightboxCaption = document.getElementById("lightbox-caption");
const lightboxClose = document.querySelector(".lightbox-close");
const metaDescription = document.querySelector('meta[name="description"]');

let currentLanguage = localStorage.getItem("preferredLanguage") || "de";
let currentGallery = [];
let currentGalleryIndex = 0;

function applyLanguage(language) {
  const selectedLanguage = translations[language] ? language : "de";
  const dict = translations[selectedLanguage];

  document.documentElement.lang = selectedLanguage;
  document.title = dict.title;
  if (metaDescription) {
    metaDescription.setAttribute("content", dict.description);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dict[key]) {
      element.textContent = dict[key];
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === selectedLanguage);
  });

  currentLanguage = selectedLanguage;
  localStorage.setItem("preferredLanguage", selectedLanguage);

  if (lightbox.open && currentGallery.length > 0) {
    renderLightboxItem(currentGalleryIndex);
  }
}

function updateVisitCounter() {
  const nextCount = Number(localStorage.getItem("visitCounter") || "0") + 1;
  localStorage.setItem("visitCounter", String(nextCount));
  visitCounter.textContent = String(nextCount);
}

function openMenu() {
  const isOpen = siteHeader.classList.toggle("is-menu-open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
}

function closeMenu() {
  siteHeader.classList.remove("is-menu-open");
  menuToggle.setAttribute("aria-expanded", "false");
}

function renderLightboxItem(index) {
  const item = currentGallery[index];
  if (!item) {
    return;
  }

  currentGalleryIndex = index;
  lightboxImage.src = item.src;
  lightboxCaption.textContent = translations[currentLanguage][item.captionKey] || "";
}

function openLightboxWithGallery(galleryItems, startIndex = 0) {
  currentGallery = galleryItems;
  renderLightboxItem(startIndex);

  if (typeof lightbox.showModal === "function") {
    lightbox.showModal();
  } else {
    lightbox.setAttribute("open", "open");
  }
}

function closeLightbox() {
  if (lightbox.open && typeof lightbox.close === "function") {
    lightbox.close();
  } else {
    lightbox.removeAttribute("open");
  }
}

function moveGallery(direction) {
  if (currentGallery.length === 0) {
    return;
  }

  const nextIndex = (currentGalleryIndex + direction + currentGallery.length) % currentGallery.length;
  renderLightboxItem(nextIndex);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang);
  });
});

menuToggle.addEventListener("click", openMenu);

siteNav.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", closeMenu);
});

document.querySelectorAll("[data-gallery]").forEach((button) => {
  button.addEventListener("click", () => {
    const galleryName = button.dataset.gallery;
    const galleryItems = apartmentGalleries[galleryName] || [];
    openLightboxWithGallery(galleryItems, 0);
  });
});

document.querySelectorAll(".gallery-item").forEach((button) => {
  button.addEventListener("click", () => {
    const imageSrc = button.dataset.full;
    const captionKey = button.dataset.captionKey;
    openLightboxWithGallery([{ src: imageSrc, captionKey }], 0);
  });
});

lightboxClose.addEventListener("click", closeLightbox);

lightbox.addEventListener("click", (event) => {
  if (event.target === lightbox) {
    closeLightbox();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && lightbox.open) {
    closeLightbox();
  }

  if (event.key === "ArrowRight" && lightbox.open) {
    moveGallery(1);
  }

  if (event.key === "ArrowLeft" && lightbox.open) {
    moveGallery(-1);
  }
});

updateVisitCounter();
applyLanguage(currentLanguage);
